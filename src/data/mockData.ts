// --------------- Computed Summaries ---------------
export function computeMetrics(articles: Article[]): MetricSummary {
  // Dynamic: recalculated against real current date every render
  const now = new Date();
  const todayStart = new Date(now);
  todayStart.setHours(0, 0, 0, 0);
  const weekStart = new Date(now);
  weekStart.setDate(weekStart.getDate() - 6);
  weekStart.setHours(0, 0, 0, 0);

  return {
    totalArticles: articles.length,
    todayCount: articles.filter((a) => new Date(a.publishedAt) >= todayStart).length,
    weekCount: articles.filter((a) => new Date(a.publishedAt) >= weekStart).length,
    confirmedCount: articles.filter((a) => a.classification === "confirmed_maternal_mortality").length,
    probableCount: articles.filter((a) => a.classification === "probable_maternal_mortality").length,
  };
}

export function computeDistrictCounts(articles: Article[]): DistrictCount[] {
  const map: Record<string, number> = {};
  articles.forEach((a) => {
    const d = a.district || "Unknown";
    map[d] = (map[d] || 0) + 1;
  });
  return Object.entries(map)
    .map(([district, count]) => ({ district, count }))
    .sort((a, b) => b.count - a.count);
}

export function computeSourceCounts(articles: Article[]): SourceCount[] {
  const map: Record<string, number> = {};
  articles.forEach((a) => {
    map[a.source] = (map[a.source] || 0) + 1;
  });
  return Object.entries(map)
    .map(([source, count]) => ({ source, count }))
    .sort((a, b) => b.count - a.count);
}

export interface HospitalCount {
  hospital: string;
  district: string;
  count: number;
  negligenceCount: number;
}

export function computeHospitalCounts(articles: Article[]): HospitalCount[] {
  const map: Record<string, HospitalCount> = {};
  articles.forEach((a) => {
    if (!a.hospitalName) return;
    const key = a.hospitalName;
    if (!map[key]) {
      map[key] = { hospital: a.hospitalName, district: a.district || "Unknown", count: 0, negligenceCount: 0 };
    }
    map[key].count++;
    if (a.negligenceFlag) map[key].negligenceCount++;
  });
  return Object.values(map).sort((a, b) => b.count - a.count);
}
