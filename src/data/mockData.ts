const ARCHIVE_START = new Date("2026-03-01T00:00:00.000Z");
function archiveDate(daysAfterStart: number, hour = 9): string {
  const d = new Date(ARCHIVE_START);
  d.setDate(d.getDate() + daysAfterStart);
  d.setHours(hour);
  return d.toISOString();
}

